• Kotlin Unit > Unit é um tipo de retorno, utilizado em funções que não tem um retorno de valor, semelhante ao void. Por exemplo, quando há uma função que apenas apresenta um "println" no fim. Sendo o uso opcional, visto que o Kotlin implicitamente coloca o Unit quando não há retorno.

• Operadores ternários > Geralmente são símbolos ou palavras próprias da sintaxe que especificam uma ou mais condições (verdadeira, falsa, maior, menor, igual, ou etc.).

O operador ternário é uma alternativa para substituir o if else em algumas situações, ele é escrito da seguinte forma:

Condição ? verdadeiro : falso

•	Condição é a condição que será testada.
•	Verdadeiro é o que fazer quando a condição for verdadeira.
•	Falso é o que fazer quando a condição for falsa.


• Modularização e principais vantagens > Modularizar é trabalhar a resolução de problemas (nos códigos), dividindo em subproblemas (menores e mais simples), assim poderá ser analisado de forma individual e mais simplista. Temos como vantagem facilitar o trabalho de problemas complexos, permitir a reutilização de módulos e podendo ser desenvolvido por diferentes equipes, favorecendo a estruturação do programa, facilitando a correção, modificação e melhorando a legibilidade do programa.

